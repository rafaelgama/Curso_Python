'''
str- string
'''

print('Essa é uma "string" (str)')
print("Essa é uma 'string' (str)")
# sar um caracter de escape para forçar a Aspas
print('Esse é meu \'TEXTO\' (str).')
print("Esse é meu \"TEXTO\" (str).")

#Utilizando o r antes do  texto, faz o print ignorar os comando dentro da expressão
# exemplo com o \n que seria uma quebra de linha
print(r'Esse é meu \n texto (str)')